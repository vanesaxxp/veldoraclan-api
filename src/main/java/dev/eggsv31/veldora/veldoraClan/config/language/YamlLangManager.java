    public YamlLangManager(JavaPlugin plugin, String language) {
        this.plugin = plugin;
        this.loadLanguageFile(language);
    }

    public void loadLanguageFile(String language) {
        this.langFile = new File(this.plugin.getDataFolder() + "/Language", language + ".yml");
        if (!this.langFile.exists()) {
            this.plugin.saveResource("Language/" + language + ".yml", false);
        }

        this.langConfig = YamlConfiguration.loadConfiguration(this.langFile);

        switch (language.toUpperCase()) {
            case "ENG":
                checkAndAddMissingMessages(messagesENG);
                saveLangFileInOrder(messagesENG);
                break;
            case "TR":
                checkAndAddMissingMessages(messagesTR);
                saveLangFileInOrder(messagesTR);
                break;
            case "RU":
                checkAndAddMissingMessages(messagesRU);
                saveLangFileInOrder(messagesRU);
                break;
            case "FR":
                checkAndAddMissingMessages(messagesFR);
                saveLangFileInOrder(messagesFR);
                break;
            case "JP":
                checkAndAddMissingMessages(messagesJP);
                saveLangFileInOrder(messagesJP);
                break;
            case "ES":
                checkAndAddMissingMessages(messagesES);
                saveLangFileInOrder(messagesES);
                break;
            default:
        }
    }

    public String getMessage(String path, String defaultMessage) {
        return this.langConfig.getString(path, defaultMessage);
    }

    public void reloadLangFile() {
        String language = this.plugin.getConfig().getString("language.messages", "ENG");
        this.loadLanguageFile(language);
    }

    private void checkAndAddMissingMessages(String[][] messages) {
        YamlConfiguration newLangConfig = new YamlConfiguration();

        for (String[] message : messages) {
            String path = message[0];

            if (this.langConfig.contains(path)) {
                newLangConfig.set(path, this.langConfig.getString(path));
            }
        }

        try {
            newLangConfig.save(this.langFile);
        } catch (IOException e) {
            e.printStackTrace();
        }

        this.langConfig = newLangConfig;
    }

    public String get(Player player, String path) {
        String value = langConfig.getString(path, path);

        if (value.contains("%player%")) {
            value = value.replace("%player%", player.getName());
        }

        value = ChatColor.translateAlternateColorCodes('&', value);

        return value;
    }



    private void saveLangFileInOrder(String[][] messages) {
        YamlConfiguration orderedConfig = new YamlConfiguration();
        for (String[] message : messages) {
            String path = message[0];
            String defaultMessage = message[1];
            orderedConfig.set(path, langConfig.getString(path, defaultMessage));
        }

        try {
            orderedConfig.save(this.langFile);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
